<script>
    $(document).ready(function() {
        // نراقب التغيير في قائمة الأقسام
        $('select[name="section_id"]').on('change', function() {
            var SectionId = $(this).val(); // نأخذ رقم القسم المختار
            if (SectionId) {
                $.ajax({
                    url: "{{ URL::to('get_products') }}/" + SectionId,
                    type: "GET",
                    dataType: "json",
                    success: function(data) {
                        // 1. نقوم بتفريغ قائمة المنتجات تماماً
                        $('select[name="product_id"]').empty();
                        // 2. نضيف خيار افتراضي
                        $('select[name="product_id"]').append('<option value="" selected disabled>حدد المنتج</option>');
                        // 3. نلف على البيانات القادمة من السيرفر ونضيفها كخيارات
                        $.each(data, function(key, value) {
                            $('select[name="product_id"]').append('<option value="' + key + '">' + value + '</option>');
                        });
                    },
                });
            }
        });
    });
</script>

<script>
    function myFunction() {
        // تأكد أن الـ IDs تطابق الـ HTML تماماً (الـ JS حساس لحالة الأحرف)
        var amount_commission = parseFloat(document.getElementById("amount_commission").value);
        var discount = parseFloat(document.getElementById("discount").value);
        var rate_vat = parseFloat(document.getElementById("rate_vat").value);
        var total_with_value_vat = parseFloat(document.getElementById("total_with_value_vat").value);
        // var rate_vat = parseFloat(document.getElementById("rate_vat").value);
        
        // تنظيف النسبة المئوية من علامة % لتحويلها لرقم
        var rate_vat_Real = parseFloat(document.getElementById("rate_vat").value.replace('%', ''));

        if (typeof amount_commission === 'undefined' || !amount_commission) {
            alert('يرجي ادخال مبلغ العمولة');
        } else {
            var amount_commission2 = amount_commission - discount;
            var value_vat = amount_commission2 * rate_vat_Real / 100;
            var total_with_value_vat = parseFloat(value_vat + amount_commission2);
            var total = parseFloat(total_with_value_vat - value_vat);

            // تقريب الأرقام لخانين عشريتين
            var sumt = parseFloat(value_vat).toFixed(2);
            var sumt1 = parseFloat(total_with_value_vat).toFixed(2);
            var sumt2 = parseFloat(total).toFixed(2);

            document.getElementById("value_vat").value = sumt;
            document.getElementById("total_with_value_vat").value = sumt1;
            document.getElementById("total").value = sumt2;
        }
    }
</script>


